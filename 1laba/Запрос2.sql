SELECT Наименование, Цена
FROM   Товары
WHERE (Цена > 4)

SELECT [Наименование товара], [Дата поставки]
FROM   Заказы
WHERE ([Дата поставки] > CONVERT(DATETIME, '2020-02-10 00:00:00', 102))

SELECT [Наименование товара], Заказчик
FROM   Заказы
WHERE ([Наименование товара] = N'Мозги')

SELECT Заказчик, [Дата поставки]
FROM   Заказы
WHERE (Заказчик = N'БГТУ')
ORDER BY [Дата поставки]